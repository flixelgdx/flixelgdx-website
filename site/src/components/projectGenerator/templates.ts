/* --------------------- lwjgl3 launcher source file ----------------------- */
function lwjgl3Launcher(o: GeneratorOptions): Record<string, string> {
  const cls = o.gameName.replace(/\s+/g, '');
  const pkg = `${o.packageName}.lwjgl3`;
  const pkgDir = pkg.replace(/\./g, '/');
  if (o.language === 'kotlin') {
    return {
      [`lwjgl3/src/main/kotlin/${pkgDir}/${cls}Lwjgl3Launcher.kt`]: [
        `package ${pkg}`,
        ``,
        `import me.stringdotjar.flixelgdx.lwjgl3.FlixelLwjgl3Launcher`,
        `import com.badlogic.gdx.backends.lwjgl3.Lwjgl3ApplicationConfiguration`,
        `import ${o.packageName}.${cls}`,
        ``,
        `fun main() {`,
        `    val config = Lwjgl3ApplicationConfiguration().apply {`,
        `        setTitle("${o.gameName}")`,
        `        setWindowedMode(640, 480)`,
        `        setForegroundFPS(60)`,
        `    }`,
        `    FlixelLwjgl3Launcher.launch(${cls}(), config)`,
        `}`,
        ``,
      ].join('\n'),
    };
  }
  if (o.language === 'groovy') {
    return {
      [`lwjgl3/src/main/groovy/${pkgDir}/${cls}Lwjgl3Launcher.groovy`]: [
        `package ${pkg}`,
        ``,
        `import me.stringdotjar.flixelgdx.lwjgl3.FlixelLwjgl3Launcher`,
        `import com.badlogic.gdx.backends.lwjgl3.Lwjgl3ApplicationConfiguration`,
        `import ${o.packageName}.${cls}`,
        ``,
        `class ${cls}Lwjgl3Launcher {`,
        `    static void main(String[] args) {`,
        `        def config = new Lwjgl3ApplicationConfiguration()`,
        `        config.setTitle("${o.gameName}")`,
        `        config.setWindowedMode(640, 480)`,
        `        config.setForegroundFPS(60)`,
        `        FlixelLwjgl3Launcher.launch(new ${cls}(), config)`,
        `    }`,
        `}`,
        ``,
      ].join('\n'),
    };
  }
  return {
    [`lwjgl3/src/main/java/${pkgDir}/${cls}Lwjgl3Launcher.java`]: [
      `package ${pkg};`,
      ``,
      `import me.stringdotjar.flixelgdx.lwjgl3.FlixelLwjgl3Launcher;`,
      `import com.badlogic.gdx.backends.lwjgl3.Lwjgl3ApplicationConfiguration;`,
      `import ${o.packageName}.${cls};`,
      ``,
      `public final class ${cls}Lwjgl3Launcher {`,
      `    public static void main(String[] args) {`,
      `        Lwjgl3ApplicationConfiguration config = new Lwjgl3ApplicationConfiguration();`,
      `        config.setTitle("${o.gameName}");`,
      `        config.setWindowedMode(640, 480);`,
      `        config.setForegroundFPS(60);`,
      `        FlixelLwjgl3Launcher.launch(new ${cls}(), config);`,
      `    }`,
      `}`,
      ``,
    ].join('\n'),
  };
}

/* ----------------------- IDE-specific dotfiles ---------------------------- */
function ideFiles(o: GeneratorOptions): Record<string, string> {
  const out: Record<string, string> = {};
  if (o.ide === 'idea') {
    out['.idea/.gitignore'] = `*\n!.gitignore\n`;
    out['.idea/runConfigurations/Run_Desktop.xml'] =
      `<component name="ProjectRunConfigurationManager">\n` +
      `  <configuration default="false" name="Run Desktop" type="Application" factoryName="Application">\n` +
      `    <option name="MAIN_CLASS_NAME" value="${o.packageName}.lwjgl3.${o.gameName.replace(/\s+/g, '')}Lwjgl3Launcher" />\n` +
      `    <module name="lwjgl3" />\n` +
      `    <option name="VM_PARAMETERS" value="-Xmx${o.heapMb}m" />\n` +
      `    <method v="2"><option name="Gradle.BeforeRunTask" enabled="true" tasks="classes" /></method>\n` +
      `  </configuration>\n` +
      `</component>\n`;
  } else if (o.ide === 'eclipse') {
    out['.classpath'] =
      `<?xml version="1.0" encoding="UTF-8"?>\n` +
      `<classpath>\n` +
      `  <classpathentry kind="src" path="core/src/main/java"/>\n` +
      `  <classpathentry kind="src" path="lwjgl3/src/main/java"/>\n` +
      `  <classpathentry kind="con" path="org.eclipse.jdt.launching.JRE_CONTAINER/org.eclipse.jdt.internal.debug.ui.launcher.StandardVMType/JavaSE-${o.javaVersion}"/>\n` +
      `  <classpathentry kind="con" path="org.eclipse.buildship.core.gradleclasspathcontainer"/>\n` +
      `  <classpathentry kind="output" path="bin/default"/>\n` +
      `</classpath>\n`;
    out['.project'] =
      `<?xml version="1.0" encoding="UTF-8"?>\n` +
      `<projectDescription>\n` +
      `  <name>${o.gameId}</name>\n` +
      `  <buildSpec>\n` +
      `    <buildCommand><name>org.eclipse.jdt.core.javabuilder</name></buildCommand>\n` +
      `    <buildCommand><name>org.eclipse.buildship.core.gradleprojectbuilder</name></buildCommand>\n` +
      `  </buildSpec>\n` +
      `  <natures>\n` +
      `    <nature>org.eclipse.jdt.core.javanature</nature>\n` +
      `    <nature>org.eclipse.buildship.core.gradleprojectnature</nature>\n` +
      `  </natures>\n` +
      `</projectDescription>\n`;
  } else if (o.ide === 'vscode') {
    out['.vscode/launch.json'] = JSON.stringify(
      {
        version: '0.2.0',
        configurations: [
          {
            type: 'java',
            name: 'Run Desktop',
            request: 'launch',
            mainClass: `${o.packageName}.lwjgl3.${o.gameName.replace(/\s+/g, '')}Lwjgl3Launcher`,
            projectName: 'lwjgl3',
            vmArgs: `-Xmx${o.heapMb}m`,
          },
        ],
      },
      null,
      2
    );
    out['.vscode/settings.json'] = JSON.stringify(
      {
        'java.configuration.runtimes': [
          {name: `JavaSE-${o.javaVersion}`, default: true},
        ],
      },
      null,
      2
    );
  }
  return out;
}

/* --------------------------- README + gitignore --------------------------- */
