/* ----------------------------------------------------------------------------
 * Project template files for the FlixelGDX project generator.
 *
 * Each builder returns a record of `{relativePath: fileContents}` that we feed
 * into JSZip. We intentionally generate textual files (build scripts, Java /
 * Groovy / Kotlin sources, settings.gradle etc) instead of bundling binary
 * Gradle wrapper jars — modern Gradle can bootstrap the wrapper jar via
 * `gradle wrapper`, which is documented in the generated README.
 * --------------------------------------------------------------------------- */

export type Language = 'java' | 'groovy' | 'kotlin';
export type IDE = 'idea' | 'eclipse' | 'vscode' | 'none';
export type Template = 'blank' | 'platformer';
export type Platform = 'desktop' | 'web' | 'android' | 'ios';

export interface GeneratorOptions {
  gameName: string;
  gameId: string;
  packageName: string;
  language: Language;
  javaVersion: number;
  flixelVersion: string;
  ide: IDE;
  template: Template;
  platforms: Platform[];
  expert: boolean;
  heapMb: number;
  jvmFlags: string;
  gradleConfig: string;
}

const GROOVY_LINE = "\n";

/* -------------------------- gradle.properties ----------------------------- */
function gradleProperties(o: GeneratorOptions): string {
  return [
    `# Generated by the FlixelGDX project generator.`,
    `# Documented here: https://flixelgdx.github.io/flixelgdx-website/docs/getting-started`,
    ``,
    `org.gradle.jvmargs=-Xms32m -Xmx512m`,
    `org.gradle.daemon=true`,
    `org.gradle.parallel=true`,
    `org.gradle.caching=true`,
    ``,
    `# FlixelGDX framework version (resolved through JitPack).`,
    `flixelVersion=${o.flixelVersion}`,
    `gameName=${o.gameName}`,
    `gameId=${o.gameId}`,
    ``,
  ].join('\n');
}

/* -------------------------- settings.gradle ------------------------------- */
function settingsGradle(o: GeneratorOptions): string {
  const subs = ['core'];
  if (o.platforms.includes('desktop')) subs.push('lwjgl3');
  if (o.platforms.includes('web')) subs.push('teavm');
  // Android & iOS modules are scaffolded only when selected.
  if (o.platforms.includes('android')) subs.push('android');
  if (o.platforms.includes('ios')) subs.push('ios');
  return [
    `rootProject.name = "${o.gameId}"`,
    ...subs.map((s) => `include "${s}"`),
    ``,
  ].join('\n');
}

/* -------------------------- root build.gradle ----------------------------- */
function rootBuildGradle(o: GeneratorOptions): string {
  const kotlinBlock =
    o.language === 'kotlin'
      ? `\n        classpath "org.jetbrains.kotlin:kotlin-gradle-plugin:1.9.24"\n`
      : ``;
  const userGradle = o.expert && o.gradleConfig.trim()
    ? `\n/* ----------- expert mode: custom gradle config ----------- */\n${o.gradleConfig}\n`
    : '';
  return [
    `/*`,
    ` * Root build script generated by the FlixelGDX project generator.`,
    ` * - Java ${o.javaVersion}+ (FlixelGDX requires 17 at minimum).`,
    ` * - Pulls the framework from JitPack (no local install needed).`,
    ` */`,
    `buildscript {`,
    `    repositories {`,
    `        mavenCentral()`,
    `        gradlePluginPortal()`,
    `        maven { url 'https://jitpack.io' }`,
    `    }`,
    `    dependencies {${kotlinBlock}    }`,
    `}`,
    ``,
    `allprojects {`,
    `    version = "0.0.1"`,
    `    group = "${o.packageName}"`,
    `    repositories {`,
    `        mavenCentral()`,
    `        google()`,
    `        maven { url 'https://jitpack.io' }`,
    `    }`,
    `}`,
    userGradle,
    ``,
  ].join('\n');
}

/* -------------------------- core build.gradle ----------------------------- */
function coreBuildGradle(o: GeneratorOptions): string {
  const langPlugin =
    o.language === 'java'
      ? `id 'java-library'`
      : o.language === 'groovy'
        ? `id 'groovy'\n    id 'java-library'`
        : `id 'org.jetbrains.kotlin.jvm'\n    id 'java-library'`;

  const langDep =
    o.language === 'groovy'
      ? `\n    implementation "org.codehaus.groovy:groovy-all:3.0.21"`
      : o.language === 'kotlin'
        ? `\n    implementation "org.jetbrains.kotlin:kotlin-stdlib:1.9.24"`
        : ``;

  return [
    `plugins {`,
    `    ${langPlugin}`,
    `}`,
    ``,
    `java {`,
    `    sourceCompatibility = JavaVersion.VERSION_${o.javaVersion}`,
    `    targetCompatibility = JavaVersion.VERSION_${o.javaVersion}`,
    `}`,
    ``,
    `dependencies {`,
    `    api "com.github.flixelgdx.flixelgdx:flixelgdx-core:\${flixelVersion}"${langDep}`,
    `}`,
    ``,
  ].join('\n');
}

/* -------------------------- lwjgl3 build.gradle --------------------------- */
function lwjgl3BuildGradle(o: GeneratorOptions): string {
  const langPlugin =
    o.language === 'kotlin'
      ? `id 'org.jetbrains.kotlin.jvm'\n    id 'application'`
      : o.language === 'groovy'
        ? `id 'groovy'\n    id 'application'`
        : `id 'application'`;
  const mainSuffix =
    o.language === 'kotlin' ? 'Kt' : '';
  const baseFlags = `-Xms${Math.max(8, Math.floor(o.heapMb / 2))}m -Xmx${o.heapMb}m`;
  const userFlags = o.expert && o.jvmFlags.trim() ? ` ${o.jvmFlags.trim()}` : '';
  return [
    `plugins {`,
    `    ${langPlugin}`,
    `}`,
    ``,
    `application {`,
    `    mainClass = "${o.packageName}.lwjgl3.${o.gameName.replace(/\\s+/g, '')}Lwjgl3Launcher${mainSuffix}"`,
    `    applicationDefaultJvmArgs = ["${baseFlags}${userFlags}".split(" ").findAll { !it.isEmpty() }]`,
    `}`,
    ``,
    `dependencies {`,
    `    implementation project(":core")`,
    `    implementation "com.github.flixelgdx.flixelgdx:flixelgdx-lwjgl3:\${flixelVersion}"`,
    `}`,
    ``,
  ].join('\n');
}

