/* ----------------------------------------------------------------------------
 * Project template files for the FlixelGDX project generator.
 *
 * Each builder returns a record of `{relativePath: fileContents}` that we feed
 * into JSZip. We intentionally generate textual files (build scripts, Java /
 * Groovy / Kotlin sources, settings.gradle etc) instead of bundling binary
 * Gradle wrapper jars. The Gradle wrapper jar is embedded separately for
 * offline use.
 *
 * JitPack publishes Flixel Gradle plugins under maven coordinates
 * (`com.github.flixelgdx.flixelgdx:flixelgdx-*-plugin`) while the plugin
 * IDs stay `me.stringdotjar.flixelgdx.*` — Gradle needs the
 * `pluginManagement.resolutionStrategy` mapping below so the plugins DSL
 * resolves (otherwise you get “plugin … not found” / apply failures).
 * --------------------------------------------------------------------------- */

/** Pin with framework / COMPILING.md (TeaVM Gradle plugin used by consumers). */
const TEAVM_GRADLE_PLUGIN_VERSION = '0.13.0';
/** Pin with Kotlin stdlib coordinate in generated `core` / `lwjgl3`. */
const KOTLIN_VERSION = '1.9.24';
/** Mirrors README “Add it with JitPack” — consumer-facing group on JitPack. */
const JITPACK_FLIXEL_COORD = 'com.github.flixelgdx.flixelgdx';
/** Matches flixelgdx-lwjgl3 POM (com.badlogicgames.gdx:gdx-backend-lwjgl3). */
const GDX_VERSION = '1.14.0';
/** GraalVM Native Build Tools (Gradle 9 compatible). */
const GRAALVM_NATIVE_PLUGIN_VERSION = '0.10.6';
/** Construo desktop packaging plugin. */
const CONSTRUO_PLUGIN_VERSION = '2.1.0';

export type Language = 'java' | 'groovy' | 'kotlin';
export type Template = 'blank' | 'platformer';
export type Platform = 'desktop' | 'web' | 'android' | 'ios';
export type JdkVendor =
  | 'graalvm'       // GraalVM Community — fast startup, JIT/AOT options
  | 'temurin'       // Eclipse Temurin (Adoptium) — standard Hotspot
  | 'corretto'      // Amazon Corretto — long-term-supported Hotspot
  | 'zulu';         // Azul Zulu — broad version coverage

export interface GeneratorOptions {
  gameName: string;
  gameId: string;
  packageName: string;
  language: Language;
  javaVersion: number;
  flixelVersion: string;
  template: Template;
  platforms: Platform[];
  jdkVendor: JdkVendor;
  expert: boolean;
  heapMb: number;
  jvmFlags: string;
  gradleConfig: string;
}

/**
 * Gradle `JvmVendorSpec` enum value matching a `JdkVendor`. See
 * https://docs.gradle.org/current/javadoc/org/gradle/jvm/toolchain/JvmVendorSpec.html
 */
export function gradleVendorSpec(v: JdkVendor): string {
  switch (v) {
    case 'graalvm': return 'GRAAL_VM';
    case 'temurin': return 'ADOPTIUM';
    case 'corretto': return 'AMAZON';
    case 'zulu': return 'AZUL';
  }
}

/** Safe inside Java / Groovy double-quoted string literals (game window title, logs, etc.). */
function escDoubleQuotedJvm(name: string): string {
  return name
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"')
    .replace(/\r/g, '\\r')
    .replace(/\n/g, '\\n');
}

/** Safe inside Kotlin double-quoted strings (also escapes $ so ${...} is not accidentally formed). */
function escDoubleQuotedKotlin(name: string): string {
  return name
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"')
    .replace(/\$/g, '\\$')
    .replace(/\r/g, '\\r')
    .replace(/\n/g, '\\n');
}

