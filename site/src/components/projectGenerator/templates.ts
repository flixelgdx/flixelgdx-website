/**
 * Loaded from {@code lwjgl3/build.gradle} when {@code enableGraalNative} is true.
 * Applies GraalVM Native Build Tools configuration in the lwjgl3 subproject context
 * so plugin resolution uses {@code settings.gradle} pluginManagement.
 */
function nativeImageGradle(): string {
  return [
    `// GraalVM Native Image (only loaded when enableGraalNative=true in gradle.properties).`,
    `// Resource patterns are adapted from https://lyze.dev/2021/04/29/libGDX-Internal-Assets-List/`,
    ``,
    `import groovy.json.JsonOutput`,
    `import java.util.Locale`,
    ``,
    `ext.appName = rootProject.findProperty('gameId') ?: rootProject.name`,
    ``,
    `def isMacOsNative = System.getProperty('os.name', '').toLowerCase(Locale.ROOT).contains('mac')`,
    ``,
    `graalvmNative {`,
    `  toolchainDetection = true`,
    `  binaries {`,
    `    main {`,
    `      imageName = appName`,
    `      mainClass = application.mainClass`,
    `      requiredVersion = '23.0'`,
    `      buildArgs.add('-march=compatibility')`,
    `      buildArgs.add('-H:+ReportExceptionStackTraces')`,
    `      if (isMacOsNative) {`,
    `        buildArgs.add('-J-XstartOnFirstThread')`,
    `      }`,
    `      jvmArgs.addAll('-Dfile.encoding=UTF8')`,
    `      sharedLibrary = false`,
    `      resources {`,
    `        autodetect()`,
    `      }`,
    `    }`,
    `  }`,
    `}`,
    ``,
    `tasks.named('run').configure {`,
    `  doNotTrackState('Running the app should not be affected by Graal.')`,
    `}`,
    ``,
    `afterEvaluate {`,
    `  tasks.matching { it.name == 'generateResourcesConfigFile' }.configureEach { gen ->`,
    `    gen.doFirst {`,
    `      def assetsFolder = rootProject.file('assets')`,
    `      def resFolder = file('src/main/resources/META-INF/native-image/' + ext.appName)`,
    `      resFolder.mkdirs()`,
    `      def resFile = new File(resFolder, 'resource-config.json')`,
    `      resFile.delete()`,
    ``,
    `      def namePatterns = []`,
    `      if (assetsFolder.exists()) {`,
    `        fileTree(assetsFolder).each { namePatterns << java.util.regex.Pattern.quote(it.name) }`,
    `      }`,
    `      def middle = namePatterns.isEmpty() ? '' : namePatterns.join('|') + '|'`,
    `      def pattern = '.*(' + middle + 'libgdx.+\\\\.png|lsans.+)'`,
    ``,
    `      def model = [`,
    `        resources: [includes: [[pattern: pattern]]],`,
    `        bundles: []`,
    `      ]`,
    `      resFile.text = JsonOutput.prettyPrint(JsonOutput.toJson(model))`,
    `    }`,
    `  }`,
    `}`,
    ``,
  ].join('\n');
}

/* -------------------------- lwjgl3 build.gradle --------------------------- */
