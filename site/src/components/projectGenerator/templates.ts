/**
 * Build the *text* files for a project. The Gradle wrapper jar (binary) is
 * added separately by {@link generateProjectZip} so JSZip can write it with
 * the right encoding.
 */
export function buildProjectFiles(o: GeneratorOptions): Record<string, string> {
  const files: Record<string, string> = {};
  files['README.md'] = readme(o);
  files['.gitignore'] = gitignore();
  files['.editorconfig'] = editorconfig();
  files['assets/.gitkeep'] =
    '# This file keeps the empty assets folder in Git so you can add images, sounds, and data later.\n';
  files['settings.gradle'] = settingsGradle(o);
  files['build.gradle'] = rootBuildGradle(o);
  files['gradle.properties'] = gradleProperties(o);
  files['core/build.gradle'] = coreBuildGradle(o);
  Object.assign(files, gameClassFiles(o));
  if (o.platforms.includes('desktop')) {
    files['lwjgl3/build.gradle'] = lwjgl3BuildGradle(o);
    files['lwjgl3/nativeimage.gradle'] = nativeImageGradle();
    const lwjgl3PkgPath = `${o.packageName}.lwjgl3`.replace(/\./g, '/');
    files[`lwjgl3/src/main/java/${lwjgl3PkgPath}/StartupHelper.java`] =
      buildStartupHelperJava(`${o.packageName}.lwjgl3`);
    Object.assign(files, lwjgl3Launcher(o));
  }
  if (o.platforms.includes('web')) {
    files['teavm/build.gradle'] = teavmBuildGradle(o);
    Object.assign(files, teavmLauncher(o));
  }

  files['gradle/wrapper/gradle-wrapper.properties'] = gradleWrapperProperties();
  return files;
}
